function changeContent(index) {
    // Atualiza o conteudo da seção "carrossel"
    updateCarrosselContent(index);
  
    // Atualiza o conteudo da seção "atracoes"
    updateAtracoesContent(index);
  }
  
  function updateCarrosselContent(index) {
    var mainImage = document.getElementById("imagebox");
    var mainText = document.getElementById("mainText");
    var mainTitle = document.getElementById("mainTitle");
    var mainSubtitle = document.getElementById("mainSubtitle");
  
    mainImage.src = imagens[index].src;
    mainText.innerHTML = imagens[index].texto;
    mainTitle.innerHTML = imagens[index].titulo;
    mainSubtitle.innerHTML = imagens[index].subtitulo;
  }
  
  function updateAtracoesContent(index) {
    var atracoesImagesPequenas = document.querySelectorAll(".atracoes__imagens-pequenas img");
    var cardMainImage = document.getElementById("card-mainImage");
    var cardMainTitle = document.getElementById("card-mainTitle");
    var cardMainSubtitle = document.getElementById("card-mainSubtitle");
    var cardMainText = document.getElementById("card-mainText");
  
    // Atualiza as imagens pequenas
    atracoesImagesPequenas.forEach(function (element, i) {
      element.src = imagens[index].subimages[i].src;
  
      // Evento para mudar o conteudo de acordo com as imagens pequenas
      element.onclick = function () {
        updateAtracoesLargeImageContent(index, i);
      };
    });
  
    // Atualiza a imagem grande e o conteudo
    updateAtracoesLargeImageContent(index, 0);
  }
  
  function updateAtracoesLargeImageContent(mainIndex, subIndex) {
    var cardMainImage = document.getElementById("card-mainImage");
    var cardMainTitle = document.getElementById("card-mainTitle");
    var cardMainSubtitle = document.getElementById("card-mainSubtitle");
    var cardMainText = document.getElementById("card-mainText");
  
    // Utiliza o indices principais e sub para atualizar o conteudo
    cardMainImage.src = imagens[mainIndex].subimages[subIndex].src;
    cardMainTitle.innerHTML = imagens[mainIndex].subimages[subIndex].titulo;
    cardMainSubtitle.innerHTML = imagens[mainIndex].subimages[subIndex].subtitulo;
    cardMainText.innerHTML = imagens[mainIndex].subimages[subIndex].texto;
  }
  
  changeContent(0);


